void main(List<String> args) {
  /**
   * OPERADORES RELACIONAIS 
   * usados para comparar valores ou expressÃµes 
   * retorna true ou false 
   * == igualdade
   * != diferente de 
   * > maior que 
   * < menor que
   * >= maior ou igual a
   * <= menor ou igual a
   */

  int a = 10;
  int b = 5;

  print(a == b); //false
  print(a != b); //true
  print(a > b); //true
  print(a < b); //false
  print(a >= b); //true
  print(a <= b); //false
}
