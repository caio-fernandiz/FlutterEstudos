void main(List<String> args) {
  /**
   * O switche case é usado quando você precisa verificar 
   * múltiplas condições para uma única expressão (variavel)
   */

  int dia = 1;
  switch (dia) {
    case 1:
      print("Segundou");
      break;

    case 2:
      print("bora trabalhar");
      break;

    default:
      print("Descansa que amanhã é segunda");
  }
}
